package in.amruthashala.momapp.inputresponsemodel;

public class DeliveryCharge {
    public String local_delivery_charge;
    public String zonal_delivery_charge;
    public String national_delivery_charge;

    public String getLocal_delivery_charge() {
        return local_delivery_charge;
    }

    public void setLocal_delivery_charge(String local_delivery_charge) {
        this.local_delivery_charge = local_delivery_charge;
    }

    public String getZonal_delivery_charge() {
        return zonal_delivery_charge;
    }

    public void setZonal_delivery_charge(String zonal_delivery_charge) {
        this.zonal_delivery_charge = zonal_delivery_charge;
    }

    public String getNational_delivery_charge() {
        return national_delivery_charge;
    }

    public void setNational_delivery_charge(String national_delivery_charge) {
        this.national_delivery_charge = national_delivery_charge;
    }
}
